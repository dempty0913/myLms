package com.sp.lms.review;

public class Review {
	private int lectureNum;
	private String lectureName;
	private String userName;
	private String major;
	private String year;
	private String semester;
	private String day;
	private String time;
	private int lectureReviewNum;
	private int lectureApplyNum;
	private String testLevel;
	private String projectLevel;
	private String content;
	private String regDate;
	
	private int check;
	
	private int reviewCount;
	
	public int getLectureNum() {
		return lectureNum;
	}
	public void setLectureNum(int lectureNum) {
		this.lectureNum = lectureNum;
	}
	public String getLectureName() {
		return lectureName;
	}
	public void setLectureName(String lectureName) {
		this.lectureName = lectureName;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getMajor() {
		return major;
	}
	public void setMajor(String major) {
		this.major = major;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getSemester() {
		return semester;
	}
	public void setSemester(String semester) {
		this.semester = semester;
	}
	public String getDay() {
		return day;
	}
	public void setDay(String day) {
		this.day = day;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public int getLectureReviewNum() {
		return lectureReviewNum;
	}
	public void setLectureReviewNum(int lectureReviewNum) {
		this.lectureReviewNum = lectureReviewNum;
	}
	public int getLectureApplyNum() {
		return lectureApplyNum;
	}
	public void setLectureApplyNum(int lectureApplyNum) {
		this.lectureApplyNum = lectureApplyNum;
	}
	public String getTestLevel() {
		return testLevel;
	}
	public void setTestLevel(String testLevel) {
		this.testLevel = testLevel;
	}
	public String getProjectLevel() {
		return projectLevel;
	}
	public void setProjectLevel(String projectLevel) {
		this.projectLevel = projectLevel;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getRegDate() {
		return regDate;
	}
	public void setRegDate(String regDate) {
		this.regDate = regDate;
	}
	public int getCheck() {
		return check;
	}
	public void setCheck(int check) {
		this.check = check;
	}
	public int getReviewCount() {
		return reviewCount;
	}
	public void setReviewCount(int reviewCount) {
		this.reviewCount = reviewCount;
	}
	
	
	
}
